import UIKit

extension UIColor {
    static var darkBlue: UIColor {
        return UIColor(red: 13/255, green: 27/255, blue: 50/255, alpha: 1)
    }
    
    static var lightBlue: UIColor {
        return UIColor(red: 20/255, green: 110/255, blue: 160/255, alpha: 1)
    }
    
}
